        title="Pengelolaan Sampah Kertas: Pentingnya Daur Ulang untuk Konservasi Sumber Daya">
        <p>Sampah kertas termasuk dalam kategori sampah non-organik yang sering dihasilkan dari aktivitas manusia. Dalam artikel ini, akan dibahas mengenai pentingnya pengelolaan sampah kertas dan upaya daur ulang yang dapat dilakukan untuk meminimalkan dampak lingkungan.</p>
        <br>

        <h2 class="text-lg font-semibold">Proses Daur Ulang Kertas</h2>
        <p>Proses daur ulang kertas melibatkan beberapa tahap, termasuk pengumpulan, pemisahan, penghancuran, pencampuran serat kertas bekas, serta pembentukan dan pengeringan lembaran kertas baru. Dalam proses ini, kertas bekas diolah menjadi bahan baku untuk pembuatan kertas baru.</p>

        <br>
        <h2 class="text-lg font-semibold">Manfaat Daur Ulang Kertas</h2>
        <p>Daur ulang kertas memiliki beberapa manfaat, antara lain:</p>
        <div class="ol pl-5">     
            <li class="list-decimal list-inside">
                <span class="font-medium">Konservasi sumber daya alam:</span> Dengan mendaur ulang kertas, penggunaan kayu sebagai bahan baku utama dapat dikurangi, sehingga membantu melindungi hutan dan sumber daya alam lainnya.
            </li>
            <li class="list-decimal list-inside">
                <span class="font-medium">Pengurangan emisi gas rumah kaca:</span> Proses pembuatan kertas baru dari serat kertas bekas menghasilkan emisi gas rumah kaca yang lebih rendah dibandingkan dengan produksi kertas baru dari bahan baku primer.
            </li>
            <li class="list-decimal list-inside">
                <span class="font-medium"> engurangan volume sampah:</span> Daur ulang kertas membantu mengurangi volume sampah yang masuk ke tempat pembuangan akhir, sehingga mengurangi tekanan terhadap tempat pembuangan sampah.
            </li>
        </div>

        <br>
        <h2 class="text-lg font-semibold">Peran Masyarakat dalam Pengelolaan Sampah Kertas</h2>
        <p>Pengelolaan sampah kertas memerlukan peran aktif dari masyarakat. Beberapa langkah yang dapat diambil antara lain:</p>
        <div class="ol pl-5">
            <li class="list-decimal list-inside">
                <span class="font-medium">Memisahkan sampah kertas:</span> Memisahkan kertas dari sampah lainnya untuk memudahkan proses daur ulang.
            </li>
            <li class="list-decimal list-inside">
                <span class="font-medium">Menggunakan kertas daur ulang:</span> Mendorong penggunaan produk kertas daur ulang sebagai alternatif untuk mengurangi permintaan akan kertas baru.
            </li>
            <li class="list-decimal list-inside">
                <span class="font-medium">Mengedukasi masyarakat:</span> Menyebarkan informasi mengenai pentingnya daur ulang kertas serta cara pengelolaan yang benar.
            </li>
        </div>

        <br>
        <p>Pengelolaan sampah kertas melalui proses daur ulang merupakan langkah penting dalam menjaga lingkungan dan konservasi sumber daya alam. Dengan peran aktif masyarakat dalam memisahkan dan mendaur ulang kertas, kita dapat mengurangi dampak negatif sampah kertas dan menciptakan lingkungan yang lebih berkelanjutan.</p>

