        title="Pengelolaan Sampah Kaca dan Kaleng: Daur Ulang untuk Lingkungan yang Lebih Bersih">
        <p>Sampah kaca dan kaleng merupakan jenis sampah non-organik yang umum dijumpai. Dalam artikel ini, kami membahas tentang pengelolaan sampah kaca dan kaleng melalui proses daur ulang, dengan fokus pada manfaat lingkungan dan pemanfaatan sumber daya.</p>
        <br>

        <h2 class="text-lg font-semibold">Proses Daur Ulang Kaca dan Kaleng</h2>
        <p>Daur ulang kaca melibatkan pengumpulan, pemisahan, pembersihan, dan peleburan kaca bekas untuk digunakan kembali dalam pembuatan produk kaca baru. Daur ulang kaleng melibatkan proses pemadatan, penghancuran, pemisahan logam, dan pemurnian untuk menghasilkan bahan baku yang dapat digunakan kembali.</p>

        <br>
        <h2 class="text-lg font-semibold">Manfaat Lingkungan dan Pemanfaatan Sumber Daya</h2>
        <p>Daur ulang sampah kaca dan kaleng memiliki beberapa manfaat, antara lain:</p>
        <div class="ol pl-5">     
            <li class="list-decimal list-inside">
                <span class="font-medium">Mengurangi penggunaan sumber daya alam:</span> Dengan mendaur ulang kaca dan kaleng, kita dapat mengurangi eksploitasi sumber daya alam yang tidak terbarukan seperti pasir dan bijih logam.
            </li>
            <li class="list-decimal list-inside">
                <span class="font-medium">Mengurangi emisi gas rumah kaca:</span> Proses produksi kaca dan logam baru memiliki dampak lingkungan yang signifikan, termasuk emisi gas rumah kaca. Daur ulang kaca dan kaleng membantu mengurangi emisi tersebut.
            </li>
            <li class="list-decimal list-inside">
                <span class="font-medium">Mengurangi jumlah sampah yang terbuang:</span> Daur ulang kaca dan kaleng membantu mengurangi volume sampah yang mencemari lingkungan dan membebani tempat pembuangan sampah.
            </li>
        </div>

        <br>
        <h2 class="text-lg font-semibold">Peran Masyarakat dalam Pengelolaan Sampah Kaca dan Kaleng</h2>
        <p>Pengelolaan sampah kaca dan kaleng membutuhkan peran aktif dari masyarakat. Beberapa langkah yang dapat diambil antara lain:</p>
        <div class="ol pl-5">
            <li class="list-decimal list-inside">
                <span class="font-medium">Memisahkan sampah:</span> Memisahkan kaca dan kaleng dari sampah rumah tangga lainnya untuk memudahkan proses daur ulang.
            </li>
            <li class="list-decimal list-inside">
                <span class="font-medium">Menggunakan tempat pengumpulan yang tepat:</span> Memanfaatkan wadah atau kontainer yang disediakan untuk mengumpulkan kaca dan kaleng secara terpisah.
            </li>
            <li class="list-decimal list-inside">
                <span class="font-medium">Mengedukasi masyarakat:</span> Menyebarkan informasi mengenai pentingnya daur ulang kaca dan kaleng serta cara pengelolaan yang benar.
            </li>
        </div>

        <br>
        <p>Pengelolaan sampah kaca dan kaleng melalui proses daur ulang memiliki manfaat signifikan bagi lingkungan dan pemanfaatan sumber daya. Dengan peran aktif masyarakat dan kesadaran akan pentingnya daur ulang, kita dapat menciptakan lingkungan yang lebih bersih dan berkelanjutan.</p>

